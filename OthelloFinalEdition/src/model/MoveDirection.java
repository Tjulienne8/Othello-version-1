package model;

/**
 * The `MoveDirection` enum represents the eight possible directions for moves in the Othello game.
 * Each direction corresponds to a specific movement on the game board.
 */
public enum MoveDirection {
    /** Upward direction on the game board.
     *  Downward direction on the game board.
     *  Leftward direction on the game board.
     *  Rightward direction on the game board.
     *  Up-left diagonal direction on the game board.
     *  Up-right diagonal direction on the game board.
     *  Down-left diagonal direction on the game board.
     *  Down-right diagonal direction on the game board.
     */
    UP, DOWN, LEFT, RIGHT,UPLEFT,UPRIGHT, DOWNLEFT, DOWNRIGHT


}
